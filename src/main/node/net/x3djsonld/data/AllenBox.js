function AllenBox
  /** Default constructor to create this object. */
  ()
  {
    /** The initialized model object, created within initialize() method. */
    this.x3dModel = {};
    this.initialize();
    return this;
  }
AllenBox.prototype = {
  /** Create and initialize the X3D model. */
  initialize : function ()
  {
  this.x3dModel = (new autoclass.X3D()).setProfile("Immersive").setVersion("4.0")
  .setHead((new autoclass.head())
    .addMeta((new autoclass.meta()).setName("title").setContent("AllenBox.x3d"))
    .addMeta((new autoclass.meta()).setName("creator").setContent("Allen Box"))
    .addMeta((new autoclass.meta()).setName("created").setContent("8 July 2025"))
    .addMeta((new autoclass.meta()).setName("license").setContent("../license.html")))
  .setScene((new autoclass.Scene())
    .addChild((new autoclass.WorldInfo()).setTitle("AllenBox.x3d"))
    .addChild((new autoclass.NavigationInfo()).setAvatarSize(java.newArray("float", [0.15,1.53,0.75])).setSpeed(0.5).setType("\"EXAMINE\""))
    .addChild((new autoclass.Transform("Floor")).setScale(1.0,0.0125,1.0).setTranslation(0.0,-0.0125,0.0)
      .addChild((new autoclass.Shape())
        .setGeometry((new autoclass.Box()))
        .setAppearance((new autoclass.Appearance())
          .setMaterial((new autoclass.Material()))))));
  },
  // end of initialize() method


  /** Provide a shallow copy of the X3D model.
   * @return AllenBox model
   */
  getX3dModel : function()
  {	  
	  return this.x3dModel;
  },
  
  /** Indicate X3DJSAIL validation results for this X3D model.
   * @return validation results plus exception information, if any
   */
  validateSelf : function()
  {
	var validationResult = "";
	var  exceptionResult = "";
	try
	{
		this.initialize();
		
		if ((this.getX3dModel() == null) || (this.getX3dModel().getHead() == null))
		{
			validationResult = "empty scene, nothing to validate. " + this.x3dModel.validate();
			return validationResult;
		}
		validationResult += this.x3dModel.validate(); // walk entire tree to validate correctness
	}
	catch (e)
	{
		exceptionResult = e; // report exception failures, if any
	}
	if  (exceptionResult === "" && validationResult === "")
	     return "success";
	else
	{
		var returnMessage = "";
		if  (exceptionResult !== "" && validationResult !== "")
			returnMessage += "\n*** ";
		returnMessage += exceptionResult;
		if  (exceptionResult === "" && validationResult !== "")
			returnMessage = "\n" + returnMessage; // skip line before meta tags, etc.
		returnMessage += validationResult;
		return returnMessage;
	}
  },
    /** Default main() method provided for test purposes.
     * @param argv input parameters
     */
    main : function (argv)
    {
		var testObject = new AllenBox();
		console.log ("AllenBox execution self-validation test results: " + testObject.validateSelf());
	}
}
new AllenBox().main();