	@Override
	public LoadSensorObject setWatchList(X3DNode[] newValue)
	{
		if (newValue == null)
		{
			clearWatchList(); // newValueNullSetDEFAULT_VALUE
			return this;
		}
		watchList.clear(); // reset
		for (int i = 0; i < newValue.length; i++)
		{
			if  (newValue[i] instanceof X3DUrlObject)
			{
				watchList.add((X3DUrlObject)newValue[i]);
				((X3DConcreteElement) newValue[i]).setParentObject(this); // parentTest12
			}
			else throw new org.web3d.x3d.sai.InvalidFieldValueException("X3DNode[] newValue["+i+"] is not instanceof X3DUrlObject; newValue=" + Arrays.toString(newValue));
		}
		return this;
	}

