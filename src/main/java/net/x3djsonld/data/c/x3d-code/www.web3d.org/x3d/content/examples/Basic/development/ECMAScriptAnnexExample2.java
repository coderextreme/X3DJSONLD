	/** Create and initialize the X3D model for this object. */
	public final void initialize()
	{
  x3dModel = new X3D().setProfile(X3D.PROFILE_IMMERSIVE).setVersion(X3D.VERSION_3_0)
  .setHead(new head()
    .addMeta(new meta().setName(meta.NAME_TITLE      ).setContent("ECMAScriptAnnexExample2.x3d"))
    .addMeta(new meta().setName(meta.NAME_CREATOR    ).setContent("Don Brutzman"))
    .addMeta(new meta().setName(meta.NAME_CREATED    ).setContent("16 January 2000"))
    .addMeta(new meta().setName(meta.NAME_MODIFIED   ).setContent("20 October 2019"))
    .addMeta(new meta().setName(meta.NAME_DESCRIPTION).setContent("A simple script example with no visible rendering results."))
    .addMeta(new meta().setName(meta.NAME_ERROR      ).setContent("VRML97 translation has extraneous 'url [ ]' output."))
    .addMeta(new meta().setName(meta.NAME_IDENTIFIER ).setContent("https://www.web3d.org/x3d/content/examples/Basic/development/ECMAScriptAnnexExample2.x3d"))
    .addMeta(new meta().setName(meta.NAME_GENERATOR  ).setContent("X3D-Edit 3.3, https://savage.nps.edu/X3D-Edit"))
    .addMeta(new meta().setName(meta.NAME_LICENSE    ).setContent("../license.html")))
  .setScene(new Scene()
    .addChild(new WorldInfo().setInfo(new String[] {"Example 2, VRML 97 Annex C, ECMAScript Scripting Reference","https://www.web3d.org/technicalinfo/specifications/vrml97/part1/javascript.html#Example","Edits: wrapped using a Transform node for completeness, renamed duplicate url field"}).setTitle("ECMAScriptAnnexExample2.wrl"))
    .addChild(new Transform("ROOT_TRANSFORM")
      .addChild(new Script("Example_2").setDirectOutput(true).setSourceCode("\n" + 
"        " + "\n" + 
"ecmascript:" + "\n" + 
"\n" + 
"function trigger_event(value, ts)" + "\n" + 
"{" + "\n" + 
"	// do something and then fetch values" + "\n" + 
"	Browser.createVRMLFromURL(url, myself, 'nodesLoaded');" + "\n" + 
"}" + "\n" + 
"function nodesLoaded(value, timestamp)" + "\n" + 
"{" + "\n" + 
"	if (value.length > 5)" + "\n" + 
"	{" + "\n" + 
"		// do something if more than 5 nodes in this MFNode array..." + "\n" + 
"	}" + "\n" + 
"	root.addChildren = value;" + "\n" + 
"}" + "\n" + 
"// example is missing reference to the url field defined above" + "\n")
        .addField(new field().setName("myself").setType(field.TYPE_SFNODE).setAccessType(field.ACCESSTYPE_INITIALIZEONLY)
          .addChild(new Script().setUSE("Example_2")))
        .addField(new field().setName("root").setType(field.TYPE_SFNODE).setAccessType(field.ACCESSTYPE_INITIALIZEONLY)
          .addChild(new Transform().setUSE("ROOT_TRANSFORM")))
        .addField(new field().setName("identifier").setType(field.TYPE_MFSTRING).setAccessType(field.ACCESSTYPE_INITIALIZEONLY).setValue(new String[] {"foo.wrl"}))
        .addField(new field().setName("nodesLoaded").setType(field.TYPE_SFNODE).setAccessType(field.ACCESSTYPE_INPUTONLY))
        .addField(new field().setName("trigger_event").setType(field.TYPE_SFBOOL).setAccessType(field.ACCESSTYPE_INPUTONLY)))));
    }
	// end of initialize() method

